sayi1 = 15
sayi2 = 4

print(f"Sayı 1: {sayi1}, Sayı 2: {sayi2}\n")

# Toplama
print(f"Toplama: {sayi1} + {sayi2} = {sayi1 + sayi2}")

# Çıkarma
print(f"Çıkarma: {sayi1} - {sayi2} = {sayi1 - sayi2}")

# Çarpma
print(f"Çarpma: {sayi1} * {sayi2} = {sayi1 * sayi2}")

# bölme
print(f"Çarpma: {sayi1} / {sayi2} = {sayi1 / sayi2}")